-- Name: check_and_award_achievement(uuid, text, jsonb); Type: FUNCTION; Schema: public; Owner: postgres
--

CREATE FUNCTION public.check_and_award_achievement(p_user_id uuid, p_badge_name text, p_metadata jsonb DEFAULT '{}'::jsonb) RETURNS boolean
    LANGUAGE plpgsql SECURITY DEFINER
    AS $$
DECLARE
    v_already_earned boolean;
BEGIN
    -- Check if user already has this achievement
    SELECT EXISTS (
        SELECT 1 
        FROM user_achievements 
        WHERE user_id = p_user_id AND badge_name = p_badge_name
    ) INTO v_already_earned;
    
    -- If not earned, award it
    IF NOT v_already_earned THEN
        INSERT INTO user_achievements (user_id, badge_name, metadata)
        VALUES (p_user_id, p_badge_name, p_metadata);
        RETURN true;
    END IF;
    
    RETURN false;
END;
$$;


