    -- Achievement challenges can only be active once until completed
    IF v_challenge.type = 'achievement' THEN
        IF EXISTS (
            SELECT 1 FROM user_achievements
            WHERE user_id = p_user_id
            AND badge_name = v_challenge.reward_badge
        ) THEN
            RETURN false;
        END IF;
    END IF;
    
    RETURN true;
END;
$$;


